function S_bar=getSbar()

global const

%-------------------------------------------------------------------------
                        % Aufgabe 3 b %
%-------------------------------------------------------------------------
h = const.h;
E = const.E;
I = const.I;
n = const.n;


S_bar = (E*I/h^3)*[12, 6*h, -12, 6*h;
                  6*h, 4*h^2, -6*h, 2*h^2;
                  -12, -6*h, 12, -6*h;
                  6*h, 2*h^2, -6*h, 4*h^2];
%-------------------------------------------------------------------------
                        % Aufgabe 4 b %
%-------------------------------------------------------------------------


S_bar = repmat(S_bar,[1,1,n]);

end