function q_bar=getqbar()

global const

%-------------------------------------------------------------------------
                        % Aufgabe 3 c %
%-------------------------------------------------------------------------
h = const.h;
q = const.q;
n = const.n;


q_bar = (q*h/12)*[6;
                  h;
                  6;
                 -h;];
%-------------------------------------------------------------------------
                        % Aufgabe 4 c %
%-------------------------------------------------------------------------

q_bar = repmat(q_bar,[1,1,n]);

end